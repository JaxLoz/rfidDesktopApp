    public Recharge getRechargeFromForm() {
        double amount = totalTxt.getNumericValue();
        if(amount < 1000){
            throw new RuntimeException("Â¡El monto minimo a recargar debe ser superar 1.000!");
        }
        Recharge recharge = new Recharge();
        recharge.setAmount(amount);
        recharge.setStudent(student);
        setVisible(false);
        return recharge;
    }

    private void loadInfo() {
        nameLb.setText(student.getFirtsName() + " " + student.getLastName());
        identificationLb.setText(student.getIdentification());
        balanceLb.setText(Formatter.formatCurrency(student.getBalance()));
    }

    public void setStudent(Student student) {
        this.student = student;
        loadInfo();
    }

