    public boolean validateLetters(String texto) {
        Pattern patron = Pattern.compile("^[a-zA-Z ]+$");
        Matcher matcher = patron.matcher(texto);
        return matcher.matches();
    }
    
     public boolean validateNumbers(String texto) {
        return texto.matches("\\d+"); // Verifica si contiene solo d√≠gitos
    }
     
    public boolean validatePost(String correo) {
        String patronCorreo = "^[a-zA-Z0-9_.]+@gmail\\.com$";
        Pattern patron = Pattern.compile(patronCorreo);
        Matcher matcher = patron.matcher(correo);
        return matcher.matches();
    }
    
    public void cleanField (){
        txtFirstName.setText("");
        txtLastName.setText("");
        txtIdentification.setText("");
        txtEmail.setText("");
        
    }
    
    public void updateUuid (String uuid){
        lblUuid.setText(uuid);
    }
    
    
    public void setInfoStudent (Student student){
        
        this.idStudent = student.getId();
        lblUuid.setText(student.getUuid());
        txtFirstName.setText(student.getFirtsName());
        txtLastName.setText(student.getLastName());
        txtIdentification.setText(student.getIdentification());
        txtEmail.setText(student.getMail());
    }
    
    public Student getInfoUpdateStudent (){
        Student updateStudent = null;
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String identification = txtIdentification.getText();
        String email = txtEmail.getText();
        String uuidNew = lblUuid.getText();
        
        updateStudent = new Student(
                this.idStudent,
                firstName,
                identification,
                lastName,
                uuidNew,
                email);
        
        return updateStudent;
    }
    
